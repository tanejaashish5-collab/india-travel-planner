#!/usr/bin/env node
/**
 * build-blog-inserts.mjs — reads markdown drafts from data/blog-drafts/
 * and emits a single .sql file with INSERT statements for the articles table.
 *
 * Expects YAML frontmatter with: id, slug, title, subtitle, category,
 * depth, reading_time, cover_image_url, tags, destinations, seo_title,
 * seo_description, excerpt. Body (after the second ---) becomes content.
 *
 * Usage:
 *   node scripts/build-blog-inserts.mjs <file1.md> <file2.md> ... > data/blog-drafts/inserts.sql
 *
 * Safety: read-only. Emits SQL to stdout — does not execute.
 */

import { readFileSync } from "fs";

function parseFrontmatter(md) {
  const m = md.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!m) throw new Error("no frontmatter");
  const fm = {};
  const body = m[2].trim();
  for (const line of m[1].split("\n")) {
    const kv = line.match(/^(\w+):\s*(.+)$/);
    if (!kv) continue;
    const key = kv[1];
    let val = kv[2].trim();
    if (val.startsWith("[") && val.endsWith("]")) {
      val = JSON.parse(val.replace(/'/g, '"'));
    } else if (val.startsWith('"') && val.endsWith('"')) {
      val = val.slice(1, -1);
    }
    fm[key] = val;
  }
  return { fm, body };
}

function sqlQuote(s) {
  if (s == null) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

function sqlTextArray(arr) {
  if (!arr || !arr.length) return "ARRAY[]::TEXT[]";
  return "ARRAY[" + arr.map(sqlQuote).join(", ") + "]";
}

function sqlJsonb(obj) {
  if (obj == null) return "NULL";
  return sqlQuote(JSON.stringify(obj)) + "::jsonb";
}

const files = process.argv.slice(2);
if (!files.length) {
  console.error("Usage: node scripts/build-blog-inserts.mjs <file1.md> ...");
  process.exit(1);
}

console.log("-- Generated by scripts/build-blog-inserts.mjs on " + new Date().toISOString().slice(0, 10));
console.log("-- Run in Supabase Dashboard SQL Editor. One transaction covers all posts.");
console.log("");
console.log("BEGIN;");
console.log("");

for (const file of files) {
  const md = readFileSync(file, "utf-8");
  const { fm, body } = parseFrontmatter(md);

  const id = fm.id;
  const slug = fm.slug;
  const title = fm.title;
  const subtitle = fm.subtitle || null;
  const category = fm.category;
  const depth = fm.depth || "brief";
  const readingTime = Number(fm.reading_time) || Math.max(2, Math.round(body.split(/\s+/).length / 220));
  const coverUrl = fm.cover_image_url || null;
  const tags = Array.isArray(fm.tags) ? fm.tags : [];
  const destinations = Array.isArray(fm.destinations) ? fm.destinations : [];
  const seoTitle = fm.seo_title || fm.title;
  const seoDescription = fm.seo_description || fm.excerpt || null;
  const excerpt = fm.excerpt || null;

  console.log(`-- ${slug}`);
  console.log(`INSERT INTO articles (`);
  console.log(`  id, slug, title, subtitle, category, depth, reading_time,`);
  console.log(`  cover_image_url, tags, destinations, seo_title, seo_description,`);
  console.log(`  excerpt, content, published_at, updated_at, featured, callouts`);
  console.log(`) VALUES (`);
  console.log(`  ${sqlQuote(id)},`);
  console.log(`  ${sqlQuote(slug)},`);
  console.log(`  ${sqlQuote(title)},`);
  console.log(`  ${sqlQuote(subtitle)},`);
  console.log(`  ${sqlQuote(category)},`);
  console.log(`  ${sqlQuote(depth)},`);
  console.log(`  ${readingTime},`);
  console.log(`  ${sqlQuote(coverUrl)},`);
  console.log(`  ${sqlTextArray(tags)},`);
  console.log(`  ${sqlJsonb(destinations)},`);
  console.log(`  ${sqlQuote(seoTitle)},`);
  console.log(`  ${sqlQuote(seoDescription)},`);
  console.log(`  ${sqlQuote(excerpt)},`);
  console.log(`  ${sqlQuote(body)},`);
  console.log(`  NOW(), NOW(), FALSE, '[]'::jsonb`);
  console.log(`) ON CONFLICT (id) DO UPDATE SET`);
  console.log(`  title = EXCLUDED.title,`);
  console.log(`  subtitle = EXCLUDED.subtitle,`);
  console.log(`  content = EXCLUDED.content,`);
  console.log(`  excerpt = EXCLUDED.excerpt,`);
  console.log(`  cover_image_url = EXCLUDED.cover_image_url,`);
  console.log(`  tags = EXCLUDED.tags,`);
  console.log(`  destinations = EXCLUDED.destinations,`);
  console.log(`  seo_title = EXCLUDED.seo_title,`);
  console.log(`  seo_description = EXCLUDED.seo_description,`);
  console.log(`  updated_at = NOW();`);
  console.log("");
}

console.log("COMMIT;");
