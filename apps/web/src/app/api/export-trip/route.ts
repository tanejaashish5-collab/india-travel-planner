import { NextRequest, NextResponse } from "next/server";
import { createClient } from "@supabase/supabase-js";

export const dynamic = "force-dynamic";

export async function POST(req: NextRequest) {
  const body = await req.json();
  const { items, month, travelers, budget, tripName } = body;

  if (!items?.length) {
    return NextResponse.json({ error: "No items in trip" }, { status: 400 });
  }

  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const key = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;
  if (!url || !key) return NextResponse.json({ error: "DB not configured" }, { status: 500 });

  const supabase = createClient(url, key);
  const destIds = items.map((i: any) => i.destinationId);

  const { data: dests } = await supabase
    .from("destinations")
    .select(`
      id, name, tagline, difficulty, elevation_m, vehicle_fit, family_stress,
      daily_cost, nearest_airport, nearest_railhead,
      state:states(name),
      confidence_cards(network, emergency, reach, safety_rating)
    `)
    .in("id", destIds);

  if (!dests) return NextResponse.json({ error: "Failed to fetch data" }, { status: 500 });

  const MONTH_NAMES = ["","January","February","March","April","May","June","July","August","September","October","November","December"];

  // Build a clean text export (printable/shareable)
  let text = `в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n`;
  text += `  ${tripName || "My Trip"}\n`;
  text += `  ${MONTH_NAMES[month]} В· ${travelers} travelers В· ${budget}\n`;
  text += `в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n`;

  let totalCost = 0;
  let totalDays = 0;

  items.forEach((item: any, idx: number) => {
    const dest = dests.find((d) => d.id === item.destinationId);
    if (!dest) return;

    const stateName = Array.isArray(dest.state) ? dest.state[0]?.name : (dest.state as any)?.name;
    const cc = Array.isArray(dest.confidence_cards) ? dest.confidence_cards[0] : dest.confidence_cards;
    const dayCost = dest.daily_cost?.[budget]?.total ?? 0;
    const stopCost = dayCost * item.days * travelers;
    totalCost += stopCost;
    totalDays += item.days;

    text += `в”Ѓв”Ѓв”Ѓ STOP ${idx + 1}: ${dest.name} (${item.days} days) в”Ѓв”Ѓв”Ѓ\n`;
    text += `${stateName} В· ${dest.difficulty} В· ${dest.elevation_m ? dest.elevation_m + 'm' : 'Plains'}\n`;
    text += `${dest.tagline}\n\n`;

    if (dest.vehicle_fit) text += `рџљ— Vehicle: ${dest.vehicle_fit}\n`;
    if (dest.family_stress) text += `рџ‘ЁвЂЌрџ‘©вЂЌрџ‘§ Family: ${dest.family_stress}\n`;

    // Infrastructure
    if (cc) {
      const network = cc.network;
      if (network) {
        const ops = [network.jio && "Jio", network.airtel && "Airtel", network.bsnl && "BSNL"].filter(Boolean);
        text += `рџ“¶ Network: ${ops.join(", ") || "Limited"}\n`;
        if (network.note) text += `   ${network.note}\n`;
      }
      if (cc.emergency?.nearest_hospital) text += `рџЏҐ Hospital: ${cc.emergency.nearest_hospital}\n`;
      if (cc.emergency?.ambulance) text += `рџљ‘ Ambulance: ${cc.emergency.ambulance}\n`;
    }

    text += `вњ€пёЏ Airport: ${dest.nearest_airport || 'N/A'}\n`;
    text += `рџљ‚ Rail: ${dest.nearest_railhead || 'N/A'}\n`;

    if (dayCost) text += `рџ’° Est. cost: в‚№${stopCost.toLocaleString()} (${item.days}d Г— ${travelers} people Г— в‚№${dayCost.toLocaleString()}/day)\n`;

    if (item.notes) text += `рџ“ќ Notes: ${item.notes}\n`;
    text += `\n`;
  });

  text += `в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n`;
  text += `  TRIP TOTAL\n`;
  text += `  ${items.length} stops В· ${totalDays} days В· ${travelers} travelers\n`;
  text += `  Estimated budget: в‚№${totalCost.toLocaleString()} (${budget})\n`;
  text += `в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n\n`;
  text += `Generated by NakshIQ\n`;
  text += `Data verified: April 2026\n`;

  return new NextResponse(text, {
    headers: {
      "Content-Type": "text/plain; charset=utf-8",
      "Content-Disposition": `attachment; filename="${(tripName || "my-trip").replace(/\s+/g, "-").toLowerCase()}.txt"`,
    },
  });
}
